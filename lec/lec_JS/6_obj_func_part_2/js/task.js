// Напишите функцию, которая принимает два числа и возвращает их сумму.
// Вызовите эту функцию с различными аргументами и выведите результаты в консоль.

function sum(a, b) {
    return a + b;
}

console.log(sum(5, 10)); // Вывод: 15

