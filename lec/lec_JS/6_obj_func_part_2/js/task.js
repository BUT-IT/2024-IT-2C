// Напишите функцию, которая принимает другую функцию (анонимную) в качестве аргумента и вызывает ее.
// Пример: функция, которая выполняет действие через 3 секунды.

function delayedAction(callback) {
    setTimeout(callback, 3000);
}

delayedAction(function() {
    console.log("Действие выполнено через 3 секунды!");
});