// Задача 1: Подсчет четных и нечетных чисел
// Напишите программу, которая подсчитывает количество четных и нечетных чисел в диапазоне от 1 до 50. Выведите результаты на экран.

let evenCount = 0;
let oddCount = 0;

for (let i = 1; i <= 50; i++) {
    if (i % 2 === 0) {
        evenCount++;
    } else {
        oddCount++;
    }
}

console.log(`Четные числа: ${evenCount}, Нечетные числа: ${oddCount}`);

// Задача 2: Факториал числа
// Создайте программу, которая запрашивает у пользователя число и вычисляет его факториал с помощью цикла for. Выведите результат на экран.

let number = prompt("Введите число:");
let factorial = 1;

for (let i = 1; i <= number; i++) {
    factorial *= i;
}

console.log(`Факториал ${number} равен ${factorial}`);

// Задача 3: Таблица умножения
// Напишите программу, которая выводит таблицу умножения для числа, введенного пользователем, от 1 до 10. Используйте цикл for для решения задачи.

let number = prompt("Введите число для таблицы умножения:");

for (let i = 1; i <= 10; i++) {
    console.log(`${number} * ${i} = ${number * i}`);
}

// Задача 4: Сумма чисел
// Создайте программу, которая запрашивает у пользователя число и вычисляет сумму всех чисел от 1 до введенного числа, используя цикл while. Выведите результат на экран.

let number = prompt("Введите число:");
let sum = 0;
let i = 1;

while (i <= number) {
    sum += i;
    i++;
}

console.log(`Сумма чисел от 1 до ${number} равна ${sum}`);

// Задача 5: Печать чисел
// Создайте программу, которая выводит все числа от 1 до 100, но заменяет:
// числа, кратные 3, на "Fizz"
// числа, кратные 5, на "Buzz"
// числа, кратные 15, на "FizzBuzz"
