// Напишите программу, которая запрашивает у пользователя количество чисел и выводит последовательность Фибоначчи до этого количества. Используйте цикл для решения задачи.
// Эти задачи помогут участникам практиковаться в использовании условных и повторяющихся конструкций, а также развить навыки программирования на JavaScript.

let count = prompt("Введите количество чисел Фибоначчи:");
let a = 0, b = 1;

console.log("Последовательность Фибоначчи:");
for (let i = 0; i < count; i++) {
    console.log(a);
    let next = a + b;
    a = b;
    b = next;
}